/**
 * One-shot tool: replace every style="..." attribute in src/gui/*.html
 * with a content-hashed CSS class, and write the matching rules to
 * src/gui/css/inline-migrated.css.
 *
 * Algorithm:
 *   1. Walk every HTML file
 *   2. For each style="..." attribute, normalize its content (trim, sort
 *      properties), hash it (sha1 -> 8 hex chars)
 *   3. Generate class name `.s-<hash>`
 *   4. Replace style="..." with class="s-<hash>" (merge if class= already exists)
 *   5. Collect all unique declarations into a single CSS file
 *
 * Idempotent on the markup -- skips elements that already use s-<hash> classes.
 *
 * Usage:
 *   node scripts/csp-extract-inline-styles.js
 */

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const GUI_DIR = path.join(__dirname, '..', 'src', 'gui');
const CSS_DIR = path.join(GUI_DIR, 'css');
const CSS_FILE = path.join(CSS_DIR, 'inline-migrated.css');

fs.mkdirSync(CSS_DIR, { recursive: true });

// Map: className -> declaration body (e.g. "color: red; margin: 4px;")
const classes = new Map();

/**
 * Normalize style declarations to make hashing stable across cosmetic
 * differences (whitespace, trailing semicolons, casing).
 */
function normalizeStyle(raw) {
  return raw
    .split(';')
    .map(s => s.trim())
    .filter(Boolean)
    .map(decl => {
      const colonIdx = decl.indexOf(':');
      if (colonIdx < 0) return decl;
      const prop = decl.slice(0, colonIdx).trim().toLowerCase();
      const val = decl.slice(colonIdx + 1).trim();
      return `${prop}: ${val}`;
    })
    .join('; ');
}

function hashStyle(normalized) {
  return crypto
    .createHash('sha1')
    .update(normalized)
    .digest('hex')
    .slice(0, 8);
}

/**
 * Process a single HTML file in place.
 */
function processFile(filePath) {
  let html = fs.readFileSync(filePath, 'utf8');
  const file = path.basename(filePath);

  // Match style="..." -- supports both single and double quotes around the attribute
  // Captures the quote char and the content separately
  const STYLE_ATTR_RE = /\bstyle=(["'])([^"']*)\1/g;

  let count = 0;
  const newHtml = html.replace(STYLE_ATTR_RE, (full, quote, body) => {
    const normalized = normalizeStyle(body);
    if (!normalized) return full; // empty style="" -- leave as is
    const hash = hashStyle(normalized);
    const className = `s-${hash}`;
    if (!classes.has(className)) {
      classes.set(className, normalized);
    }
    count++;
    // Returning a class= here -- the merge with existing class= attributes is
    // handled in a second pass below to avoid regex back-references getting messy.
    return `data-csp-class="${className}"`;
  });

  // Second pass: merge data-csp-class into class= (or create class=) for each tag
  // We walk tag by tag using a tag-finder regex.
  const TAG_RE = /<([a-zA-Z][^>]*?)>/g;
  const merged = newHtml.replace(TAG_RE, (full, attrs) => {
    if (!attrs.includes('data-csp-class=')) return full;

    // Extract the data-csp-class value(s) -- there can only be one per tag because
    // each tag had at most one style="..."
    const dataMatch = attrs.match(/\s*data-csp-class="([^"]+)"/);
    if (!dataMatch) return full;
    const newClass = dataMatch[1];
    let cleanedAttrs = attrs.replace(/\s*data-csp-class="[^"]+"/, '');

    const classMatch = cleanedAttrs.match(/\sclass=(["'])([^"']*)\1/);
    if (classMatch) {
      // Merge with existing class
      const existing = classMatch[2].split(/\s+/).filter(Boolean);
      if (!existing.includes(newClass)) existing.push(newClass);
      cleanedAttrs = cleanedAttrs.replace(
        /\sclass=(["'])([^"']*)\1/,
        ` class="${existing.join(' ')}"`
      );
    } else {
      // Add class attribute
      cleanedAttrs = cleanedAttrs + ` class="${newClass}"`;
    }
    return `<${cleanedAttrs}>`;
  });

  if (count > 0) {
    fs.writeFileSync(filePath, merged);
    console.log(`OK   ${file}: replaced ${count} inline styles`);
  }
  return count;
}

const files = fs
  .readdirSync(GUI_DIR)
  .filter(f => f.endsWith('.html'));

let total = 0;
for (const file of files) {
  total += processFile(path.join(GUI_DIR, file));
}

// Write the CSS file
const cssLines = [
  '/* Auto-generated by scripts/csp-extract-inline-styles.js during S6 CSP migration. */',
  '/* Each .s-<hash> class corresponds to one unique inline style="..." declaration. */',
  '',
];
for (const [className, decl] of [...classes.entries()].sort()) {
  cssLines.push(`.${className} { ${decl}; }`);
}
fs.writeFileSync(CSS_FILE, cssLines.join('\n') + '\n');

console.log(`\nTotal: ${total} inline styles replaced`);
console.log(`Unique classes generated: ${classes.size}`);
console.log(`CSS file: ${CSS_FILE}`);
